function FormatDate (published_date) {
  
  const date = new Date(published_date);
  const dateString = `${date.toLocaleDateString()} ${date.toLocaleTimeString()}`;
  const dateObj = new Date(dateString);
  const formattedDate = dateObj.toLocaleDateString('en-US', {
    month: 'long',
    day: 'numeric',
    year: 'numeric'
  });
  const [a, timeString, meridiem] = dateString.split(' ');
  const newTimeString = timeString.slice(0, -3);
  const dateTimeString = [formattedDate, newTimeString, meridiem].join(' ');

  return dateTimeString
  
}


